package net.safester.application.util;

/**
 * Container for JXTreeTable of group list
 * @author Alexandre Becquereau
 */
public class GroupTreeTableElement {

    //Id of group (-1) if element is member of group
    private int id;
    //Group name or member email
    private String label;
    //Id of group if element is member -1 otw
    private int groupId;
    private String thumbnail;

    public int getGroupId() {
        return groupId;
    }

    public void setGroupId(int groupId) {
        this.groupId = groupId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }
    
    /**
     * @return the thumbnail
     */
    public String getThumbnail() {
        return thumbnail;
    }    
    
    public void setThumbnail(String thumbnail) {
        this.thumbnail = thumbnail;
    }
    
    @Override
    public String toString(){
        return this.label;
    }
}
